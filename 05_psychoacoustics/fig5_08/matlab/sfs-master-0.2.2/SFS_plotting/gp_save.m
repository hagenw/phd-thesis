function gp_save(file,x,y,header)
% GP_SAVE save x,y as a text file in a Gnuplot compatible format
%
%   Usage: gp_save(file,x,y,[header])
%
%   Input parameters:
%       file    - filename of the data file
%       x       - x axis values
%       y       - y axis values [vector or matrix]
%       header  - header comment added before the data
%
%   GP_SAVE(file,x,y,header) saves the values of x and y in a text file in a
%   format useable by Gnuplot
%
%   see also: gp_save_matrix

%*****************************************************************************
% Copyright (c) 2010-2012 Quality & Usability Lab                            *
%                         Deutsche Telekom Laboratories, TU Berlin           *
%                         Ernst-Reuter-Platz 7, 10587 Berlin, Germany        *
%                                                                            *
% This file is part of the Sound Field Synthesis-Toolbox (SFS).              *
%                                                                            *
% The SFS is free software:  you can redistribute it and/or modify it  under *
% the terms of the  GNU  General  Public  License  as published by the  Free *
% Software Foundation, either version 3 of the License,  or (at your option) *
% any later version.                                                         *
%                                                                            *
% The SFS is distributed in the hope that it will be useful, but WITHOUT ANY *
% WARRANTY;  without even the implied warranty of MERCHANTABILITY or FITNESS *
% FOR A PARTICULAR PURPOSE.                                                  *
% See the GNU General Public License for more details.                       *
%                                                                            *
% You should  have received a copy  of the GNU General Public License  along *
% with this program.  If not, see <http://www.gnu.org/licenses/>.            *
%                                                                            *
% The SFS is a toolbox for Matlab/Octave to  simulate and  investigate sound *
% field  synthesis  methods  like  wave  field  synthesis  or  higher  order *
% ambisonics.                                                                *
%                                                                            *
% http://dev.qu.tu-berlin.de/projects/sfs-toolbox       sfstoolbox@gmail.com *
%*****************************************************************************


%% ===== Checking of input  parameters ==================================
nargmin = 3;
nargmax = 4;
narginchk(nargmin,nargmax);
if nargin==nargmax-1
    header = '';
end
isargchar(file);
isargvector(x);
isargmatrix(y);
isargchar(header);


%% ===== Computation =====================================================

% Ensure we have column vectors
[x1,x2] = size(x);
if x1<x2
    x = x';
    y = y';
end
% Check if the data has the same length
if size(x,1)~=size(y,1)
    error('%s: x, y has to have the same length!',upper(mfilename));
end

% Write header to the file
fid = fopen(file,'w');
fprintf(fid,'# Data file generated by gp_save.m\n');
fprintf(fid,['# ',header,'\n']);
fclose(fid);
% Append the data to the file using tabulator as a delimiter between the data
if isoctave
    dlmwrite(file,[x y],'\t','-append');
else
    dlmwrite(file,[x y],'delimiter','\t','-append');
end
